class Book {
    constructor(id, title, author, rating, cost, categories, createdAt, updatedAt, image_url) {
        this.id = id;
        this.title = title;
        this.author = author;
        this.rating = rating;
        this.cost = cost;
        this.categories = categories;
        this.createdAt = createdAt;
        this.updatedAt = updatedAt;
        this.image_url = image_url;
    }
}